/**
*   Write a function to reverse an array.

    Examples =>
        reverse([1, 2, 3, 4]) ➞ [4, 3, 2, 1]
        reverse([9, 9, 2, 3, 4]) ➞ [4, 3, 2, 9, 9]
        reverse([]) ➞ []

 */

function reverse(array) {
  let a = array.reverse();
  return a;
}
let rev = reverse([1, 2, 3, 4, 8]);
console.log("Reversed array is", rev);
